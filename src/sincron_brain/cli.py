def _detect_provider_from_key(key: str) -> str | None:
    """Best-effort detection of the provider from the API key's known prefix.

    Returns None for providers whose keys don't carry a distinguishable shape
    (Mistral, Cohere, Voyage, Azure, Ollama, custom) — for those the user
    must also export LLM_PROVIDER or rely on the provider-specific env var.
    """
    key = key.strip()
    if not key:
        return None
    if key.startswith("sk-ant-"):
        return "anthropic"
    if key.startswith(("sk-proj-", "sk-svcacct-")):
        return "openai"
    if key.startswith("sk-"):
        return "openai"
    if key.startswith("AIza"):
        return "google"
    if key.startswith(("AKIA", "ASIA")):
        return "bedrock"
    return None


def _detect_provider_from_env() -> str | None:
    """Pick a provider from the env.

    Priority:
      1. LLM_PROVIDER (paired with LLM_API_KEY) — explicit canonical path.
      2. LLM_API_KEY whose prefix uniquely identifies a provider.
      3. First supported provider whose specific env var is set (legacy).
    """
    canonical_key = os.environ.get(LLM_API_KEY_ENV, "")
    canonical_provider = os.environ.get(LLM_PROVIDER_ENV, "").strip().lower()
    if canonical_provider in PROVIDER_API_KEY_ENV and canonical_key:
        return canonical_provider
    if canonical_key:
        sniffed = _detect_provider_from_key(canonical_key)
        if sniffed is not None:
            return sniffed
    for provider, env_var in PROVIDER_API_KEY_ENV.items():
        if os.environ.get(env_var):
            return provider
    return None


def _judge_provider_override() -> str | None:
    """Explicit override that beats both --provider flag detection and env-key detection."""
    value = os.environ.get(JUDGE_PROVIDER_OVERRIDE_ENV, "").strip().lower()
    if not value:
        return None
    if value not in PROVIDER_API_KEY_ENV:
        console.print(
            f"[yellow]Ignoring {JUDGE_PROVIDER_OVERRIDE_ENV}={value!r}[/] — "
            f"not a supported provider ({', '.join(PROVIDER_API_KEY_ENV)})."
        )
        return None
    return value


def _judge_model_override() -> str | None:
    value = os.environ.get(JUDGE_MODEL_OVERRIDE_ENV, "").strip()
    return value or None


def _describe_detection_source(provider: str) -> str:
    """Explain in plain words which env var caused the detection — used in connect output."""
    canonical_key = os.environ.get(LLM_API_KEY_ENV, "")
    canonical_provider = os.environ.get(LLM_PROVIDER_ENV, "").strip().lower()
    if canonical_key and canonical_provider == provider:
        return f"{LLM_API_KEY_ENV} + {LLM_PROVIDER_ENV}={provider}"
    if canonical_key and _detect_provider_from_key(canonical_key) == provider:
        return f"{LLM_API_KEY_ENV} (looks like {provider} key)"
    return f"{PROVIDER_API_KEY_ENV[provider]} in the environment"


def _apply_judge_config(config: VaultConfig, provider: str, model: str | None = None) -> None:
    """Mutate config.judge to point at a new provider/model and persist it to disk."""
    config.judge.provider = provider
    config.judge.model = model or PROVIDER_DEFAULT_MODEL[provider]
    config.judge.api_key_env = PROVIDER_API_KEY_ENV[provider]
    config.save()


def _create_vault(
    vault_path: Path,
    provider: str | None,
    yes: bool,
) -> VaultConfig:
    console.print(f"[bold]Creating vault at:[/] {vault_path}")

    override = _judge_provider_override()
    detected = _detect_provider_from_env()
    if provider:
        chosen_provider = provider
    elif override:
        chosen_provider = override
        console.print(
            f"[green]Using[/] [bold]{override}[/] from {JUDGE_PROVIDER_OVERRIDE_ENV} override."
        )
    elif detected:
        chosen_provider = detected
        source = _describe_detection_source(detected)
        console.print(
            f"[green]Detected[/] {source} — using [bold]{detected}[/] as the judge provider."
        )
    elif yes:
        chosen_provider = next(iter(PROVIDER_API_KEY_ENV))
        console.print(
            "[yellow]No provider API key detected in the environment.[/] "
            "Vault will be created with a placeholder judge config — see the "
            "Judge section below for how to point it at a real provider."
        )
    else:
        chosen_provider = _prompt_provider()

    api_key_env = PROVIDER_API_KEY_ENV[chosen_provider]
    model = _judge_model_override() or PROVIDER_DEFAULT_MODEL[chosen_provider]

    if not yes and not os.environ.get(api_key_env):
        console.print(
            f"[yellow]Warning:[/] {api_key_env} not set in environment. "
            f"Set it before running sleep_now()."
        )

    config = VaultConfig(
        vault_path=vault_path,
        judge=JudgeConfig(
            provider=chosen_provider,
            model=model,
            api_key_env=api_key_env,
        ),
    )
    storage.ensure_vault(config)
    with storage.open_db(config):
        pass
    config.save()
    return config


@app.command()
def init(
    path: Annotated[
        Path | None, typer.Option("--path", help="Vault directory. Default: user data dir.")
    ] = None,
    provider: Annotated[
        str | None, typer.Option("--provider", help="LLM judge provider (anthropic, openai, ...).")
    ] = None,
    yes: Annotated[
        bool, typer.Option("--yes", "-y", help="Skip prompts, use defaults.")
    ] = False,
) -> None:
    """Create a new memory vault."""
    vault_path = path or _default_vault_path()
    vault_path = vault_path.expanduser().resolve()

    if (vault_path / "_config.toml").exists():
        console.print(f"[yellow]Vault already exists at {vault_path}[/]")
        raise typer.Exit(1)

    config = _create_vault(vault_path, provider, yes)

    console.print("[green]Vault created.[/]")
    console.print(f"  Config: {config.config_file}")
    console.print(f"  Index:  {config.index_db}")
    console.print(f"  Judge:  {config.judge.provider} / {config.judge.model}")
    _print_default_major_tags()
    _print_judge_status(config)
    console.print()
    console.print("[bold]Connect a project to this vault:[/]")
    console.print(f'  sincron-brain connect --path "{vault_path}"')


@app.command()
def connect(
    path: Annotated[
        Path | None,
        typer.Option("--path", help="Vault directory. Default: <project>/memory."),
    ] = None,
    project: Annotated[
        Path, typer.Option("--project", help="Project directory where .mcp.json will be written.")
    ] = Path("."),
    provider: Annotated[
        str | None, typer.Option("--provider", help="LLM judge provider if a vault is created.")
    ] = None,
    yes: Annotated[
        bool, typer.Option("--yes", "-y", help="Skip prompts, use defaults if a vault is created.")
    ] = True,
) -> None:
    """Create/use a vault and write .mcp.json for the current project."""
    project_path = project.expanduser().resolve()

    if not project_path.exists():
        console.print(f"[red]Project directory not found:[/] {project_path}")
        raise typer.Exit(1)

    vault_path = (path if path is not None else project_path / "memory").expanduser().resolve()

    if (vault_path / "_config.toml").exists():
        config = load_config(vault_path)
        console.print(f"[green]Using existing vault:[/] {config.vault_path}")
        if provider:
            _apply_judge_config(config, provider)
            console.print(
                f"[green]Judge updated:[/] {config.judge.provider}/{config.judge.model}"
            )
    else:
        config = _create_vault(vault_path, provider, yes)
        console.print("[green]Vault created.[/]")

    mcp_file = _write_project_mcp_config(project_path, config)
    claude_settings = _sync_claude_project_settings(project_path)
    instruction_files = _sync_agent_instruction_files(project_path)
    viewer_path = _write_initial_viewer(config)
    env_file = _write_env_template_if_missing(config)
    gitignore_file = _write_vault_gitignore(config)
    console.print(f"[green]MCP config written:[/] {mcp_file}")
    console.print(f"[green]Claude project settings synced:[/] {claude_settings}")
    for instruction_file in instruction_files:
        console.print(f"[green]Agent instructions synced:[/] {instruction_file}")
    console.print(f"[green]Viewer ready:[/] {viewer_path}")
    console.print(f"[green]Env template:[/] {env_file}  [dim](edit to add LLM_API_KEY)[/]")
    console.print(f"[green]Vault .gitignore:[/] {gitignore_file}")
    _print_default_major_tags()
    _print_judge_status(config)
    console.print()
    console.print("[bold]Next steps:[/]")
    console.print("  1. Restart your MCP client/agent.")
    console.print("  2. Run: sincron-brain stats")


@app.command("set-judge")
def set_judge(
    provider: Annotated[
        str | None,
        typer.Option(
            "--provider",
            help=f"Judge provider. Supported: {', '.join(PROVIDER_API_KEY_ENV)}.",
        ),
    ] = None,
    model: Annotated[
        str | None,
        typer.Option(
            "--model",
            help="Judge model. Defaults to the canonical model for the chosen provider.",
        ),
    ] = None,
    auto: Annotated[
        bool,
        typer.Option(
            "--auto",
            help="Detect the provider from the first supported API key env var that is set.",
        ),
    ] = False,
) -> None:
    """Update the judge provider/model for an existing vault without recreating it.

    Useful when you develop with one LLM (e.g. Claude in the agent) but want
    the indexing judge to run on another provider (e.g. a cheaper or local one),
    or when you set the API key only after `connect` ran.

    Use `--auto` to pick whichever supported provider key is already exported
    in this shell — handy after a fresh install where the key only landed
    afterwards.
    """
    if auto and provider:
        console.print("[red]Use either --auto or --provider, not both.[/]")
        raise typer.Exit(1)
    if not auto and not provider:
        console.print(
            "[red]Pass --auto (detect from env) or --provider <name>.[/]\n"
            f"Supported providers: {', '.join(PROVIDER_API_KEY_ENV)}."
        )
        raise typer.Exit(1)

    if auto:
        detected = _detect_provider_from_env()
        if detected is None:
            console.print(
                "[red]No supported provider API key found in the environment.[/]\n"
                "Export one of: "
                + ", ".join(PROVIDER_API_KEY_ENV.values())
                + ", then re-run with --auto."
            )
            raise typer.Exit(1)
        provider = detected
        console.print(
            f"[green]Detected[/] {PROVIDER_API_KEY_ENV[detected]} — "
            f"using [bold]{detected}[/] as the judge provider."
        )

    if provider not in PROVIDER_API_KEY_ENV:
        console.print(
            f"[red]Unsupported provider:[/] {provider}. "
            f"Pick one of: {', '.join(PROVIDER_API_KEY_ENV)}."
        )
        raise typer.Exit(1)

    config = _load_or_die()
    _apply_judge_config(config, provider, model)
    console.print(
        f"[green]Judge updated:[/] {config.judge.provider}/{config.judge.model} "
        f"(key {config.judge.api_key_env})"
    )
    _print_judge_status(config)
    console.print()
    console.print(
        "[bold]Reminder:[/] restart your MCP client so the subprocess picks up the new judge."
    )



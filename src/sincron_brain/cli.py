def _write_env_template_if_missing(config: VaultConfig) -> Path:
    """Drop a .env template the first time a vault is connected, never overwrite."""
    dotenv_path = config.vault_path / DOTENV_FILENAME
    if dotenv_path.exists():
        return dotenv_path
    dotenv_path.write_text(DOTENV_TEMPLATE, encoding="utf-8")
    try:
        from sincron_brain import storage

        storage._restrict_owner_access(dotenv_path)
    except (OSError, AttributeError):
        pass
    return dotenv_path


def _write_vault_gitignore(config: VaultConfig) -> Path:
    """Make sure the vault's .gitignore protects secrets and rebuildable artifacts."""
    gi_path = config.vault_path / ".gitignore"
    if not gi_path.exists():
        gi_path.write_text(VAULT_GITIGNORE_BODY, encoding="utf-8")
    return gi_path


